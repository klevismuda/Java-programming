package Day07_arithmetic_operators_casting;

public class PrimitiveCasting {
    public static void main(String[] args) {
        int a=115;
        byte b=(byte)a;
        short c=(short)a;
        System.out.println(b);
        System.out.println(c);

        byte b1=120;
        int a1=b1;
        System.out.println(a1);
//TODO: more practice for casting

    }
}
